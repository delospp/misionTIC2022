const PI = 3.1416;

function sumar (a,b){
    return a+b;
}
function restar (a,b){
    return a-b;
}
function multiplicar (a,b){
    return a*b;
}
function dividir (a,b){
    return a/b;
}

exports.sumar = sumar;
exports.restar = restar;
exports.dividir = dividir;
exports.multiplicar = multiplicar;
exports.PI = PI; 